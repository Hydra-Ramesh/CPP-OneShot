#include<iostream>
using namespace std;
int main(){
    // Auto Keyword help to detect datatype of variable by itself
    auto userId=1234; // int
    auto height=5.6; // double
    auto name="John"; // const char*
    auto isPaid=true; // bool
    
    cout<<userId<<endl;
    cout<<height<<endl;
    cout<<name<<endl;
    cout<<isPaid<<endl;
}