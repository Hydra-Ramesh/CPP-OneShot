#include<iostream>
using namespace std;
int main(){
    int x=5;
    int y=3;
    cout<<(x==y)<<endl; // It will Print 0
    cout<<(x!=y)<<endl; // It will Print 1
    cout<<(x>y)<<endl; // It will Print 1
    cout<<(x<y)<<endl; // It will Print 0
    cout<<(x>=y)<<endl; // It will Print 1
    cout<<(x<=y)<<endl; // It will Print 0
    return 0;
}