#include<iostream>
using namespace std;
int main(){
    int x=10;
    // All Called Short Hand Assignment Operators
    x+=5; // Same as x = x + 5
    x-=3; // Same as x = x - 3
    x*=2; // Same as x = x * 2
    x/=4; // Same as x = x / 4
    x%=3; // Same as x = x % 3
    cout<<x<<endl;
    return 0;
}